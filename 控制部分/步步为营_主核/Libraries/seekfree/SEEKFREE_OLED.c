/*****************************************************************************
 函 数 名  : double （其实是float，改成double将出错）
 功能描述  : 坐标、double数、小数位数
 输入参数  :
 输出参数  : none
 返 回 值  : none
*****************************************************************************/
void OLED_double(uint16 a,uint16 b,double number,char precision)
{
  int16 decimal,integer,count=0;
  OLED_P6x8Str(a,b,"      ");
  if(number<0){
    OLED_P6x8Str(a,b,"-");
    number = -number;
    a+=6;
  }
  integer = (int16)number;
  count = OLED_int16(a,b,integer);     //Integer Display
  a+=6*count;
  decimal = (int16)((number - integer)*pow(10,precision));
  if(precision!=0)
  {
    OLED_P6x8Str(a,b,".");
    for(count=precision-1;count>=0;count--)
    {
      OLED_int16(a+6*(count+1),b,decimal%10);
      decimal /= 10;
    }
  }
}

//写数据的函数
void OLED_WrDat(uint8 data)
{
  uint8 i=8;
  OLED_DCH;
  OLED_SCLL;
  while(i--)
  {
    if(data&0x80){OLED_SDAH;}
    else{OLED_SDAL;}
    OLED_SCLH;
    asm("nop");
    OLED_SCLL;
    data<<=1;
  }
}
//写指令的函数
void OLED_WrCmd(uint8 cmd)
{
  uint8 i=8;
  OLED_DCL;
  OLED_SCLL;
  while(i--)
  {
    if(cmd&0x80){OLED_SDAH;}
    else{OLED_SDAL;}
    OLED_SCLH;;
    asm("nop");
    OLED_SCLL;
    cmd<<=1;
  }
}
//设置坐标的函数
void OLED_Set_Pos(uint8 x, uint8 y)
{
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f)/*|0x01*/);
}

//清屏的函数
void OLED_CLS(void)
{
  uint8 y,x;
  for(y=0;y<8;y++)
  {
    OLED_WrCmd(0xb0+y);
    OLED_WrCmd(0x01);
    OLED_WrCmd(0x10);
    for(x=0;x<X_WIDTH;x++)
      OLED_WrDat(0);
  }
}

